/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    //timer schedules fn to execute after t milliseconds.

    /*cancelFn—returned from cancellable—can be called to prevent the execution by 
    clearing the scheduled timeout.*/

    let timer = setTimeout(() => fn(...args), t);
    return function cancelFn(){
        //If cancelFn is not called within t ms, fn is executed.
        //If cancelFn is called before t ms, fn is not executed.
        clearTimeout(timer);
    }


};

